/* 
 * A quick reminder about variable names.
 * 
 * Also, notice the use of escape sequences to format 
 * text.
 */
public class VariableNames
{
  public static void main(String[] args)
  {
    System.out.println("There are a few rules for variable names:");
    System.out.print("\t1. Variable names should be descriptive and\\or meaningful when possible;\n"
                    +"\t2. Variable names CANNOT start with a number or special symbol;\n"
                    +"\t3. Variable names are case sensitive, that is, firstName and firstname are different;\n"
                    +"\t4. If a variable name is more than one word, \"camelCase\" should be used (notice the first letter\n"
                    +"\t   is lower case and the first letter of each subsequent word is capitalized;\n"   
                    +"\t5. Sometimes temporary variables are needed for a counter or calculation, in these cases\n"
                    +"\t   variable names like i, j, n, x, etc. are acceptable.\n");
  }
}
                        